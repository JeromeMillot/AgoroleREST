package fr.agrorole.dnd.exceptions;

public class CaracFieldsException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2413251266202417263L;

	public CaracFieldsException() {
		super();
	}

	public CaracFieldsException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CaracFieldsException(String message, Throwable cause) {
		super(message, cause);
	}

	public CaracFieldsException(String message) {
		super(message);
	}

	public CaracFieldsException(Throwable cause) {
		super(cause);
	}
	
	

}
